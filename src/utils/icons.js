/**
 * Returns an inline SVG string for the named icon. `options.class`
 * is applied to the <svg> element (use the .vn-icon-* sizing
 * utilities from premium.css). Returns an empty string for an
 * unknown icon name rather than throwing — a missing icon should
 * never break page rendering.
 */
